package com.home.projectapp.dto;

public class SellerUserDto {
    private Long id;
    private String username;
    private String password;
    private String email;
    private String restaurant_name;
    private String restaurant_specific;
    private String adress;
    private Integer phonenumber;

    public SellerUserDto() {}

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getRestaurant_name() {
        return restaurant_name;
    }

    public void setRestaurant_name(String restaurant_name) {
        this.restaurant_name = restaurant_name;
    }

    public String getRestaurant_specific() {
        return restaurant_specific;
    }

    public void setRestaurant_specific(String restaurant_specific) {
        this.restaurant_specific = restaurant_specific;
    }

    public String getAdress() {
        return adress;
    }

    public void setAdress(String adress) {
        this.adress = adress;
    }

    public Integer getPhonenumber() {
        return phonenumber;
    }

    public void setPhonenumber(Integer phonenumber) {
        this.phonenumber = phonenumber;
    }
}
